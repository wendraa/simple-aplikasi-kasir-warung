    private void prosesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_prosesActionPerformed
        // TODO add your handling code here:
        int total = 0;
        int jmlhcurry = Integer.parseInt(curry.getText());
        
        if(jmlhcurry>0)
        {
            int hargacurry = jmlhcurry * 45000;
            total = total + hargacurry;
        } 
        else {
            total = total + 0;
        }
         int jmlhyakisoba = Integer.parseInt(yakisoba.getText());
        
        if(jmlhyakisoba>0)
        {
            int hargayakisoba = jmlhyakisoba * 45300;
            total = total + hargayakisoba;
        } 
        else {
            total = total + 0;
        }
         int jmlhtempura = Integer.parseInt(tempura.getText());
        
        if(jmlhtempura>0)
        {
            int hargatempura = jmlhtempura * 20000;
            total = total + hargatempura;
        } 
        else {
            total = total + 0;
        }
         int jmlhkatsu = Integer.parseInt(katsu.getText());
        
        if(jmlhkatsu>0)
        {
            int hargakatsu = jmlhkatsu * 20000;
            total = total + hargakatsu;
        } 
        else {
            total = total + 0;
        }
         int jmlhocha = Integer.parseInt(ocha.getText());
        
        if(jmlhocha>0)
        {
            int hargaocha = jmlhocha * 8000;
            total = total + hargaocha;
        } 
        else {
            total = total + 0;
        }
         int jmlhsoda = Integer.parseInt(soda.getText());
        
        if(jmlhsoda>0)
        {
            int hargasoda = jmlhsoda * 15000;
            total = total + hargasoda;
        } 
        else {
            total = total + 0;
        }
         int jmlhmacha = Integer.parseInt(macha.getText());
        
        if(jmlhmacha>0)
        {
            int hargamacha = jmlhmacha * 15000;
            total = total + hargamacha;
        } 
        else {
            total = total + 0;
        }
        totall.setText(String.valueOf(total));
    }//GEN-LAST:event_prosesActionPerformed

